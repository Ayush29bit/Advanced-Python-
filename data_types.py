# Integers
print(8)
print(type(8))

print(1e308) 

# Floating-point numbers
print(5.32)
print(type(5.32))
print(1.7e-5)

# Booleans
print(True)
print(type(True))
print(False)
print(type(False))

# Strings
print(" hello world ")
print(type(" hello world "))
print('Python is fun!')
print("""This is a multi-line
string.""")

# LISTS
print([1,2,3,4,5])
print(type([1,2,3,4,5]))
print(["apple", "banana", "cherry"])
print([1, "hello", 3.14, True])

# TUPLES
print((1,2,3))
print(type((1,2,3)))
print(("apple", "banana", "cherry"))
print((1, "hello", 3.14, False))

# DICTIONARIES
print({"name": "Alice", "age": 30})
print(type({"name": "Alice", "age": 30}))
print({"fruit": "apple", "quantity": 5, "price": 0.99})
print({"is_student": False, "grades": [90, 85, 88]})

# SETS
print({1, 2, 3, 4, 5})
print(type({1, 2, 3, 4, 5}))
print({"apple", "banana", "cherry"})
print({1, "hello", 3.14, True})
